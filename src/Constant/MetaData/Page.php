    public static function getMeta(string $routeName, string $storeName): array
    {
        $pageKey = self::getPageKeyByRoute($routeName);
        $meta = self::META_DATA[$pageKey] ?? [];

        $defaultStoreName = self::defaultStoreName;

        array_walk_recursive($meta, function (&$value) use ($defaultStoreName, $storeName) {
            if (is_string($value)) {
                $value = str_replace($defaultStoreName, $storeName, $value);
            }
        });

        return $meta;
    }

    public static function getPageKeyByRoute(string $route): string
    {
        return str_replace('_', '-', $route);
    }
